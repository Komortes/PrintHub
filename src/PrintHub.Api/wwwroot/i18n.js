  function resolveLanguage(language) {
    return translations[language] ? language : DEFAULT_LANGUAGE;
  }

  function loadStoredLanguage() {
    try {
      return resolveLanguage(localStorage.getItem(STORAGE_KEY) || DEFAULT_LANGUAGE);
    } catch {
      return DEFAULT_LANGUAGE;
    }
  }

  function persistLanguage(language) {
    try {
      localStorage.setItem(STORAGE_KEY, resolveLanguage(language));
    } catch {}
  }

  function getValue(language, key) {
    const selected = translations[resolveLanguage(language)] || {};
    return selected[key] ?? translations[DEFAULT_LANGUAGE][key] ?? key;
  }

  function format(template, params = {}) {
    return String(template).replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? `{${key}}`));
  }

  function t(key, params = {}, language = DEFAULT_LANGUAGE) {
    return format(getValue(language, key), params);
  }

  function applyTranslationsToDom(language) {
    const resolved = resolveLanguage(language);
    document.documentElement.lang = resolved;

    document.querySelectorAll('[data-i18n]').forEach(element => {
      element.textContent = t(element.dataset.i18n, {}, resolved);
    });

    document.querySelectorAll('[data-i18n-html]').forEach(element => {
      element.innerHTML = t(element.dataset.i18nHtml, {}, resolved);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
      element.setAttribute('placeholder', t(element.dataset.i18nPlaceholder, {}, resolved));
    });

    document.querySelectorAll('[data-i18n-title]').forEach(element => {
      element.setAttribute('title', t(element.dataset.i18nTitle, {}, resolved));
    });
  }

  window.PrintHubI18n = {
    t,
    loadStoredLanguage,
    persistLanguage,
    applyTranslationsToDom
  };
